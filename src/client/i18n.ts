export type I18nKey = keyof typeof zh;
export type Translator = (key: I18nKey, vars?: Record<string, string | number>) => string;

const dictionaries: Record<AppLanguage, Record<I18nKey, string>> = { zh, en };

export function createTranslator(language: AppLanguage): Translator {
  return (key, vars) => {
    let value = dictionaries[language][key] ?? dictionaries.zh[key] ?? key;
    if (!vars) return value;
    for (const [varKey, varValue] of Object.entries(vars)) {
      value = value.replaceAll(`{${varKey}}`, String(varValue));
    }
    return value;
  };
}

