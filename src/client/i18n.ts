export function translateServerMessage(message: string, t: Translator) {
  const exactKey = serverMessageKeys[message];
  if (exactKey) return t(exactKey);
  const invalidPrefix = "消息格式错误：";
  if (message.startsWith(invalidPrefix)) return t("serverInvalidMessage", { error: message.slice(invalidPrefix.length) });
  return message;
}

